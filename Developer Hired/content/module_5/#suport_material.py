## ARITHMETIC OPERATORS

# ADIÇÃO --> +
# SUBTRAÇÃO --> -
# MULTIPLICAÇÃO --> *
# DIVISÃO --> /
# POTÊNCIA --> **
# DIVISÃO INTEIRA --> //
# RESTO DA DIVISÃO --> %

#--

## ORDER OF ORIGIN

# 1- ()
# 2- **
# 3- * -> / -> // -> %
# 4- + -> -

## EXAMPLES

somar = 3 + 2
diminuir = 3 - 2
multiplicar = 3*2
dividir = 3/2
potência = 3**2
divisão_inteira = 3//2
resto_divisão = 3%2

